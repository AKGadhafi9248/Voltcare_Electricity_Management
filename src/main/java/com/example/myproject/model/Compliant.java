package com.example.myproject.model;



import java.time.LocalDate;



public class Compliant {

  private int complaintId;

  private long consumerNumber;

  private String complaintType;

  private String category;

  private String contactPerson;

  private String landmark;

  private String problemDescription;

  private String mobile;

  private String address;

  private LocalDate complaintDate;

  private String status;



  // Getter and Setter for complaintId

  public int getComplaintId() {

    return complaintId;

  }



  public void setComplaintId(int complaintId) {

    this.complaintId = complaintId;

  }



  // Getter and Setter for consumerNumber

  public long getConsumerNumber() {

    return consumerNumber;

  }



  public void setConsumerNumber(long consumerNumber) {

    this.consumerNumber = consumerNumber;

  }



  // Getter and Setter for complaintType

  public String getComplaintType() {

    return complaintType;

  }



  public void setComplaintType(String complaintType) {

    this.complaintType = complaintType;

  }



  // Getter and Setter for category

  public String getCategory() {

    return category;

  }



  public void setCategory(String category) {

    this.category = category;

  }



  // Getter and Setter for contactPerson

  public String getContactPerson() {

    return contactPerson;

  }



  public void setContactPerson(String contactPerson) {

    this.contactPerson = contactPerson;

  }



  // Getter and Setter for landmark

  public String getLandmark() {

    return landmark;

  }



  public void setLandmark(String landmark) {

    this.landmark = landmark;

  }



  // Getter and Setter for problemDescription

  public String getProblemDescription() {

    return problemDescription;

  }



  public void setProblemDescription(String problemDescription) {

    this.problemDescription = problemDescription;

  }



  // Getter and Setter for mobile

  public String getMobile() {

    return mobile;

  }



  public void setMobile(String mobile) {

    this.mobile = mobile;

  }



  // Getter and Setter for address

  public String getAddress() {

    return address;

  }



  public void setAddress(String address) {

    this.address = address;

  }



  // Getter and Setter for complaintDate

  public LocalDate getComplaintDate() {

    return complaintDate;

  }



  public void setComplaintDate(LocalDate complaintDate) {

    this.complaintDate = complaintDate;

  }



  // Getter and Setter for status

  public String getStatus() {

    return status;

  }



  public void setStatus(String status) {

    this.status = status;

  }



}